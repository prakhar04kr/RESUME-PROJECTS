function sqIndex(sq: Square): number {
  const file = sq.charCodeAt(0) - 97; // a..h => 0..7
  const rank = parseInt(sq[1], 10);   // 1..8
  return (8 - rank) * 8 + file;       // a8 = 0
}

export function evaluate(game: Chess): number {
  if (game.isCheckmate()) {
    return game.turn() === "w" ? -100000 : 100000;
  }
  if (game.isDraw() || game.isStalemate() || game.isThreefoldRepetition()) {
    return 0;
  }

  const board = game.board();
  let score = 0;
  for (let r = 0; r < 8; r++) {
    for (let f = 0; f < 8; f++) {
      const piece = board[r][f];
      if (!piece) continue;
      const v = VALUES[piece.type];
      const idx = r * 8 + f; // r=0 is rank 8
      const pstIdx = piece.color === "w" ? idx : (7 - r) * 8 + f;
      const pst = PST[piece.type][pstIdx];
      const sign = piece.color === "w" ? 1 : -1;
      score += sign * (v + pst);
    }
  }
  return score;
}
